public class Pertemuan3D {

    public static void main(String[] args) {
        char inisial = 'D';
        
        switch (inisial) {
            case 'A':
                System.out.println("Nama yang dicari Anton");
                break;
            case 'B':
                System.out.println("Nama yang dicari Boni");
                break;
            case 'C':
                System.out.println("Nama yang dicari Cica");
                break;
            case 'D':
                System.out.println("Nama yang dicari Darren");
                break;
            default:
                System.out.println("Nama yang anda cari tidak ada");
        }
    }
    
}
